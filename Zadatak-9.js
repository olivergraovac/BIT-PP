//Write a JavaScript program to check two given numbers and print “true” if one of the number is 50 or if their sum is 50.
//Sample Input: 5,54 Output : -
//Sample Input: 6,50 Output : true
//Sample Input: 40,10 Output : true

var x = 40;
var y = 10;

if(x, y === 50 || x + y === 50) {
    console.log(true);
}
else console.log(`-`);